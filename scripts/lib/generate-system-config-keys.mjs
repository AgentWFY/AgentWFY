import { readFileSync, writeFileSync } from 'fs'
import { join } from 'path'

function toCamelCase(name) {
  const stripped = name.replace(/^system\./, '')
  const parts = stripped.split(/[.-]/)
  return parts.map((p, i) => i === 0 ? p : p.charAt(0).toUpperCase() + p.slice(1)).join('')
}

export function generateSystemConfigKeys(src) {
  const jsonPath = join(src, 'system-config', 'system-config.json')
  const items = JSON.parse(readFileSync(jsonPath, 'utf-8'))

  const seen = new Set()
  const lines = []
  for (const item of items) {
    const key = toCamelCase(item.name)
    if (seen.has(key)) throw new Error(`Duplicate camelCase key "${key}" generated from "${item.name}"`)
    seen.add(key)
    lines.push(`  ${key}: ${JSON.stringify(item.name)},`)
  }

  const body = `// AUTO-GENERATED by scripts/build from src/system-config/system-config.json
// DO NOT EDIT — your changes will be overwritten on the next build.

export const SYSTEM_PREFIX = 'system.'
export const PLUGIN_PREFIX = 'plugin.'
export const SHORTCUT_PREFIX = 'system.shortcuts.'
export const OPENAI_COMPATIBLE_PREFIX = 'system.openai-compatible-provider'

export const SystemConfigKeys = {
${lines.join('\n')}
} as const

export type SystemConfigKey = typeof SystemConfigKeys[keyof typeof SystemConfigKeys]
`

  const outPath = join(src, 'system-config', 'keys.ts')
  const existing = (() => { try { return readFileSync(outPath, 'utf-8') } catch { return null } })()
  if (existing !== body) writeFileSync(outPath, body)
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const src = process.argv[2]
  if (!src) {
    console.error('Usage: generate-system-config-keys.mjs <src-dir>')
    process.exit(1)
  }
  generateSystemConfigKeys(src)
}
